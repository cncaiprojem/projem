def test_file_upload_download():
    """Test file upload and download operations."""
    print("\nğŸ“¤ Testing file upload/download...")

    try:
        s3 = S3Service()

        # Create a test file
        with tempfile.NamedTemporaryFile(mode="w", suffix=".txt", delete=False) as f:
            test_content = (
                "This is a test file for S3 functionality verification.\nGenerated by smoke test."
            )
            f.write(test_content)
            test_file_path = Path(f.name)

        try:
            # Upload the test file
            bucket = "artefacts"
            object_key = "tests/smoke_test_file.txt"

            print(f"  ğŸ“¤ Uploading test file to {bucket}/{object_key}")
            s3.upload_file(
                local_path=test_file_path,
                bucket=bucket,
                object_key=object_key,
                content_type="text/plain",
                metadata={"test": "smoke_test", "purpose": "verification"},
            )
            print("  âœ… File uploaded successfully")

            # Download the file to verify
            with tempfile.NamedTemporaryFile(mode="r", suffix=".txt", delete=False) as f:
                download_path = Path(f.name)

            print(f"  ğŸ“¥ Downloading file to verify content")
            s3.download_file(bucket, object_key, download_path)

            # Verify content
            downloaded_content = download_path.read_text()
            if downloaded_content == test_content:
                print("  âœ… File content verified successfully")
            else:
                print("  âŒ File content mismatch")
                return False

            # Get object info
            print("  ğŸ“‹ Retrieving object metadata")
            info = s3.get_object_info(bucket, object_key)
            print(f"      Size: {info['size']} bytes")
            print(f"      Content-Type: {info['content_type']}")
            print(f"      Last Modified: {info['last_modified']}")
            print(f"      Metadata: {info['metadata']}")

            # Clean up test object
            print("  ğŸ§¹ Cleaning up test file")
            s3.delete_object(bucket, object_key)
            print("  âœ… Test file deleted")

        finally:
            # Clean up local files
            test_file_path.unlink(missing_ok=True)
            download_path.unlink(missing_ok=True)

        print("âœ… File upload/download test completed")
        return True

    except Exception as e:
        print(f"âŒ File upload/download test failed: {e}")
        return False


